import pyautogui  
#Automação do teclado

#Aperta alt para baixo

pyautogui.keyDown('alt')

#Pressiona o TAB

pyautogui.press (['tab'])

#Solta a tecla Alt  

pyautogui.keyUp('alt')

#Digita o texto desejado 

pyautogui.write(' Olá Sou uma automatização desenvolvida pelo Python :) Estou te Visualizando')


